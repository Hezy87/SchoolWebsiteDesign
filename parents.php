				<h1 class="centered">FREE SCHOOL MEALS INFORMATION</h1>
				<hr>
				
				<div class="col-lg-6">
					<h2>Important Information</h2>
					<p>The approval of your free school meals application may mean you also get financial help with:</p>
                	<ul class="information">
                    	<li>Music tuition</li>
                        <li>Curriculum related residential trips</li>
                        <li>School visits</li>
                    </ul>
					<h2>How much can I save?</h2>
					<blockquote>School meals for one child currently cost &pound;1.90 per day; which adds up to &pound;9.50 each week.</blockquote>
					
					<h2>Eligibility</h2>
					<p>To apply for free school meals for your child, you must live in Newcastle and be in receipt of one of the following qualifying benefits:</p>
					<ul class="information">
						<li>Income Support</li>
						<li>Income-based Jobseeker's Allowance</li>
						<li>Income-related employment and support allowance</li>
						<li>Support from the National Asylum Support Service under part VI of the Immigration and Asylum Act 1999</li>
						<li>Child Tax Credit and not in receipt of Working Tax Credit and a household income below &pound;16,190</li>
						<li>The Guarantee element of State Pension Credit</li>
					</ul>
					<p>If you are a young person who receives Income Support or income based Jobseeker's Allowance in your own right, you are also entitled to receive free school meals. </p>
				</div><!-- col-lg-6 -->
				
				<div class="col-lg-6">
					<h2>How to apply?</h2>
					<p>To apply use the online application form available <a href="https://eforms.newcastle.gov.uk/popup.aspx/RenderForm/?F.Name=r6akBG3MJCZ" target="_blank">here</a></p>
					<p> For further infomation visit Newcastle City Council's website <a href="http://www.newcastle.gov.uk/education-and-learning/grants/free-school-meals" target="_blank">Newcastle City Council's website</a></p>
    	

					<h2>What happens next?</h2>
					<p>Newcastle City Council, Student Services will:</p>
					<ul class="information">
						<li>Check your eligibility for free school meals on an online checking system, provided by the Department for Education (Please, ensure your National Insurance number is on your application).</li>
						<li>Contact you to ask you to provide more information should it be needed before a decision is made.</li>
						<li>Write to tell you the outcome of your application and what date your child's free school meals will start and will also let the school know.</li>
					</ul>
					<p>Successful claims will start from the date Newcastle City Council receive your application, Student Services receive your application or the date handed in at the school, providing that proof of benefit can be confirmed. </p>
					<p>If your claim is unsuccessful and you think it has been wrongly assessed, please write to the Head of Access, Children's Services Directorate, Newcastle City Council, Civic Centre, Newcastle NE1 8PU or phone 0191 211 5346.</p>
